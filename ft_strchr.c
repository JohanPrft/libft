#include <stdlib.h>

char	*ft_strchr(const char *s, int c)
{
	char	*str;

	str = (char *)s;
	while (*str != (char)c)
	{
		str++;
		if ((char)c != '\0' && *str == '\0')
			return (NULL);
	}
	return (str);
}
